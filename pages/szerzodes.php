    <p>A bérlés költségei:</p>
    <ul>
        <li>bérleti díj - minden járműnél külön feltüntetve </li>
        <li>kaució</li>
    </ul>
    <p>A bérleti díj tartalmazza:</p>
    <ul>
            <li>kötelező biztosítás</li>       
            <li>magyarországi autópálya matrica</li>       
            <li>nemzetközi zöldkártya</li>        
            <li>évszaknak megfelelő gumiabroncs</li>          
            <li>tiszta állapotban történő átadás</li>       
            <li>rendeltetésszerű használatból eredő javítási költségek</li>        
    </ul>
    <p>A bérleti díj nem tartalmazza</p>
    <ul>
        <li>üzemanyag költség</li>
        <li>gumijavítás (a gumiabroncs sérülése vagy használhatatlanná válása esetén a javítási díj vagy a pótlás a bérlőt terheli)</li>
        <li>a nem rendeltetésszerű használatból eredő javítási költségeket</li>
        <li>külföldi autópálya matricákat, külföldi útdíjakat valamint parkolási díjakat</li>
    </ul>
    <p>Kaució összege:​​</p>
    <ul>
        <li>A bérleti díj és a kaució összege előre fizetendő.</li>
        <li>A kaució a bérlet lejáratakor visszajár, amennyiben az autóban kár nem keletkezett</li>
        <li>Ha a Bérlő az bérautót nem tiszta állapotban adja át kívül-belül akkor a takarítási díj a kaucióból kerül levonásra. (Kisteherautó esetén minimum: 5 000 Ft)</li>
        <li>Ha a Bérlő a bérautót nem tele tankkal adja át akkor a hiányzó üzemanyag értéke a kaucióból kerül levonásra</li>       
    </ul>
    <p>Engedélyezett futásteljesítmény:</p>
    <ul>
    <li>
    napi 500 km (kivéve egyedi akciók) Túlfutás esetén 40Ft+Áfa/km díjat számítunk fel.
    Több napos bérlés esetén 50km-es túlfutásig felárat nem számítunk fel.</li>
    </ul>
    <p>Bérleti időszak:</p>
    <ul>
        <li>megállapodás szerint</li>
    </ul>
    <p>Jármű átvétele, visszahozása:</p>
    <ul>
        <li>akár előző este is átveheti a járművet, mi rugalmasan kezeljük az átvételt és a visszahozást is. Kérjük igényét előre jelezze!</li>
    </ul>
    <p>Bérlés során vállalja, hogy az autót eredeti/átvett állapotában szulgáltatja vissza részünkre:</p>
    <ul>
        <li>sérülésmentesen,</li>
        <li>megtankulva (Az előírt üzemanyaggal!),</li>
        <li>lemosva,</li>
        <li>kitakarítva</li>
    </ul>
    <p>Bérlő területi jogosultsága:</p>
    <ul>
        <li>az autót csak a szerződésben foglalt személy használhatja Magyarország területén belül</li>
        <li>külföldi használat megegyezéstől függően</li>    
    </ul>
    <p>Bérlő nem jogosult a járművet:</p>
    <ul>
        <li>autóversenyzésre, személyszállításra használni</li>
        <li> albérletbe adni</li>
        <li>nem a rendeltetésének megfelelően használni, pl.: ulyan rakományt szállítani amire a jármű nem használható vagy nem megengedett</li>
        <li>a hatályos közlekedésrendészeti szabályoktól eltérő módon használni.</li>
    </ul>
    <h3>Dohányzás az autóban nem megengedett, a büntetés költsége: 30 000 Ft + ÁFA.</h3>
    <p>Gépjármű meghibásodása esetén kérjük, hogy azonnal hívjon telefonon minket.</p>
    <p>A bérleti szerződésben foglaltak nem teljesítése, illetve megszegése esetén azonnali felmondás esete lép életbe.</p>
    <p>Tilos a járművet az ellenőrző műszerek tiltó jelzése esetén használni!</p>
    <p>A jármű bármilyen helytelen/szabálytalan használata esetén a Bérlő a következményekért teljes körűen felel.</p>
    <p>A Bérlő köteles megtéríteni minden olyan rendőrségi, vagy más hatóság által kiszabott szabálysértési, - közigazgatási jellegű vagy egyéb bírság, -büntetés, illetve pótdíj (pl. meg nem fizetett parkolási díjat) a Bérbeadónak, amely a bérlési időszak alatt keletkezett</p>
    <p>Autóinkat műholdas védelmi rendszerrel láttuk el.</p>
